package Package_Java_Examples;

public class LoopingstatementEx {

	public static void main(String[] args) {
		//For Loop
		int i;
		int j=0;
		for(i=0; i<5; i++);
		{
			System.out.println(i);
		}
		
		//While Loop
		while(j<5)
		{
			System.out.println("Value of i is equal to 5");
			break;
		}
		j++;
		
	}

}
